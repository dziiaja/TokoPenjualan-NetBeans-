    public void loadData() {
        model.getDataVector().removeAllElements();
        
        model.fireTableDataChanged();
        
        try {
            Connection c = Koneksi.getKoneksi();
            Statement s = c.createStatement();
            
            String sql = "SELECT * FROM barang";
            ResultSet r = s.executeQuery(sql);
            
            while(r.next()) {
                Object[] o = new Object[7];
                o [0] = r.getString("ID_Barang");
                o [1] = r.getString("Nama_Barang");
                o [2] = r.getString("Jenis");
                o [3] = r.getString("Ukuran");
                o [4] = r.getString("HargaBeli");
                o [5] = r.getString("HargaJual");
                o [6] = r.getString("Stok");
                
                model.addRow(o);
            }
            r.close();
            s.close();
        } catch (Exception e) {
            System.out.println("Terjadi Kesalahan");
        }
    }
    
    public void cariData() {
        model.setRowCount(0); // Clear existing rows in the model
        try {
            Connection c = Koneksi.getKoneksi();
            String sql = "SELECT * FROM barang WHERE ID_Barang LIKE '%" + jTextField1.getText() + "%' OR Nama_Barang LIKE '%" + jTextField1.getText() + "%'";
            Statement stat = c.createStatement();
            ResultSet rs = stat.executeQuery(sql);

            while (rs.next()) {
                model.addRow(new Object[] {
                    rs.getString("ID_Barang"),
                    rs.getString("Nama_Barang"),
                    rs.getString("Jenis"),
                    rs.getString("Ukuran"),
                    rs.getString("HargaBeli"),
                    rs.getString("HargaJual"),
                    rs.getString("Stok"),
                });
            }
        } catch (Exception e) {
            System.out.println("Cari Data Error");
        }
    }
